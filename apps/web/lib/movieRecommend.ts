/** 与えられたジャンルから「こんな人におすすめ」の行を 2〜3 個生成する。 */
export function buildRecommendations(
  genres: string[],
  maxLines = 3,
): string[] {
  const cleaned = genres.map((g) => (g ?? "").trim()).filter(Boolean);
  const lines: string[] = [];
  const seen = new Set<string>();

  for (const g of cleaned) {
    const line = GENRE_RECOMMENDATIONS[g] ?? `${g}ジャンルが好きな方`;
    if (!seen.has(line)) {
      seen.add(line);
      lines.push(line);
    }
    if (lines.length >= maxLines) break;
  }

  // ジャンル情報が全く無い作品でも 1 行は出す (SSR コンテンツを空にしない)。
  if (lines.length === 0) {
    lines.push("ショート動画で気軽に作品を試し見したい方");
  }
  return lines;
}
